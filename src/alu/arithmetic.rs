/// Arithmetic ops for ALU.
/// Each functions works on unsigned integer arithmetic on the given arguments.
pub fn add(x: u32, y: u32) -> u32 {
    x + y
}

pub fn sub(x: u32, y: u32) -> u32 {
    x - y
}

pub fn mul(x: u32, y: u32) -> u32 {
    x * y
}

pub fn div(x: u32, y: u32) -> u32 {
    x / y
}